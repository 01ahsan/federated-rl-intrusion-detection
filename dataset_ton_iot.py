import pandas as pd
from sklearn.preprocessing import LabelEncoder, StandardScaler

url = "https://raw.githubusercontent.com/AmnahMubeen/TON_IoT-Network-Dataset/main/Train_Test_Network.csv"
df = pd.read_csv(url)

df = df.dropna()
df = df.select_dtypes(include=['number', 'object'])  # drop datetime cols

label_encoder = LabelEncoder()
df['label'] = label_encoder.fit_transform(df['label'])

scaler = StandardScaler()
X = df.drop("label", axis=1)
X_scaled = scaler.fit_transform(X)

df_scaled = pd.DataFrame(X_scaled, columns=X.columns)
df_scaled["label"] = df["label"]

df_scaled.to_csv("TON_IoT_cleaned.csv", index=False)
print("Saved as TON_IoT_cleaned.csv")